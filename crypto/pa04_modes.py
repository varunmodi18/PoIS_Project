"""
PA#4 — Modes of Operation: CBC, OFB, Randomized CTR.

All modes use AES_PRF from PA#2 as the block cipher E_k.

Dependencies:
    - crypto.pa02_prf (AES_PRF)
    - crypto.pa03_cpa_enc (pad_message, unpad_message)
    - crypto.utils
"""

from crypto.pa02_prf import AES_PRF
from crypto.pa03_cpa_enc import pad_message, unpad_message, BLOCK_SIZE
from crypto.utils import random_bytes, xor_bytes, bytes_to_int, int_to_bytes


# ============================================================
# CBC Mode
# ============================================================

def cbc_encrypt(key: bytes, message: bytes, iv: bytes = None,
                aes: AES_PRF = None) -> tuple[bytes, bytes]:
    """
    CBC mode encryption.

    Returns:
        (iv, ciphertext)
    """
    aes = aes or AES_PRF()
    iv = iv or random_bytes(BLOCK_SIZE)
    padded = pad_message(message)
    blocks = [padded[i:i + BLOCK_SIZE] for i in range(0, len(padded), BLOCK_SIZE)]

    prev = iv
    ct_blocks = []
    for mi in blocks:
        ci = aes.encrypt_block(key, xor_bytes(prev, mi))
        ct_blocks.append(ci)
        prev = ci

    return (iv, b''.join(ct_blocks))


def cbc_decrypt(key: bytes, iv: bytes, ciphertext: bytes,
                aes: AES_PRF = None) -> bytes:
    """
    CBC mode decryption.
    """
    aes = aes or AES_PRF()
    blocks = [ciphertext[i:i + BLOCK_SIZE] for i in range(0, len(ciphertext), BLOCK_SIZE)]

    prev = iv
    pt_blocks = []
    for ci in blocks:
        mi = xor_bytes(aes.decrypt_block(key, ci), prev)
        pt_blocks.append(mi)
        prev = ci

    return unpad_message(b''.join(pt_blocks))


# ============================================================
# OFB Mode
# ============================================================

def ofb_encrypt(key: bytes, message: bytes, iv: bytes = None,
                aes: AES_PRF = None) -> tuple[bytes, bytes]:
    """
    OFB mode encryption.
    Keystream is generated by chaining E_k on the IV.
    Encryption and decryption are identical.

    Returns:
        (iv, ciphertext)
    """
    aes = aes or AES_PRF()
    iv = iv or random_bytes(BLOCK_SIZE)
    padded = pad_message(message)
    blocks = [padded[i:i + BLOCK_SIZE] for i in range(0, len(padded), BLOCK_SIZE)]

    o = iv
    ct_blocks = []
    for mi in blocks:
        o = aes.encrypt_block(key, o)  # Generate keystream block
        ci = xor_bytes(o, mi)
        ct_blocks.append(ci)

    return (iv, b''.join(ct_blocks))


def ofb_decrypt(key: bytes, iv: bytes, ciphertext: bytes,
                aes: AES_PRF = None) -> bytes:
    """
    OFB mode decryption. Identical to encryption (XOR is its own inverse).
    """
    aes = aes or AES_PRF()
    blocks = [ciphertext[i:i + BLOCK_SIZE] for i in range(0, len(ciphertext), BLOCK_SIZE)]

    o = iv
    pt_blocks = []
    for ci in blocks:
        o = aes.encrypt_block(key, o)
        mi = xor_bytes(o, ci)
        pt_blocks.append(mi)

    return unpad_message(b''.join(pt_blocks))


# ============================================================
# Randomized CTR Mode
# ============================================================

def ctr_encrypt(key: bytes, message: bytes, nonce: bytes = None,
                aes: AES_PRF = None) -> tuple[bytes, bytes]:
    """
    Randomized Counter mode encryption.

    Returns:
        (nonce, ciphertext)
    """
    aes = aes or AES_PRF()
    nonce = nonce or random_bytes(BLOCK_SIZE)
    padded = pad_message(message)
    blocks = [padded[i:i + BLOCK_SIZE] for i in range(0, len(padded), BLOCK_SIZE)]

    nonce_int = bytes_to_int(nonce)
    ct_blocks = []
    for i, mi in enumerate(blocks):
        counter = nonce_int + i
        counter_bytes = int_to_bytes(counter, BLOCK_SIZE)
        ci = xor_bytes(aes.encrypt_block(key, counter_bytes), mi)
        ct_blocks.append(ci)

    return (nonce, b''.join(ct_blocks))


def ctr_decrypt(key: bytes, nonce: bytes, ciphertext: bytes,
                aes: AES_PRF = None) -> bytes:
    """CTR decryption. Same as encryption (XOR is its own inverse)."""
    aes = aes or AES_PRF()
    blocks = [ciphertext[i:i + BLOCK_SIZE] for i in range(0, len(ciphertext), BLOCK_SIZE)]

    nonce_int = bytes_to_int(nonce)
    pt_blocks = []
    for i, ci in enumerate(blocks):
        counter = nonce_int + i
        counter_bytes = int_to_bytes(counter, BLOCK_SIZE)
        mi = xor_bytes(aes.encrypt_block(key, counter_bytes), ci)
        pt_blocks.append(mi)

    return unpad_message(b''.join(pt_blocks))


# ============================================================
# Unified API
# ============================================================

def encrypt(mode: str, key: bytes, message: bytes,
            iv_or_nonce: bytes = None) -> tuple[bytes, bytes]:
    """Unified encryption API. mode: 'CBC', 'OFB', or 'CTR'."""
    mode = mode.upper()
    if mode == 'CBC':
        return cbc_encrypt(key, message, iv=iv_or_nonce)
    elif mode == 'OFB':
        return ofb_encrypt(key, message, iv=iv_or_nonce)
    elif mode == 'CTR':
        return ctr_encrypt(key, message, nonce=iv_or_nonce)
    else:
        raise ValueError(f"Unknown mode: {mode}. Use CBC, OFB, or CTR.")


def decrypt(mode: str, key: bytes, iv_or_nonce: bytes,
            ciphertext: bytes) -> bytes:
    """Unified decryption API."""
    mode = mode.upper()
    if mode == 'CBC':
        return cbc_decrypt(key, iv_or_nonce, ciphertext)
    elif mode == 'OFB':
        return ofb_decrypt(key, iv_or_nonce, ciphertext)
    elif mode == 'CTR':
        return ctr_decrypt(key, iv_or_nonce, ciphertext)
    else:
        raise ValueError(f"Unknown mode: {mode}. Use CBC, OFB, or CTR.")


